package mobile.tests.core.enums;

/**
 * DeviceType is enum with all possible type of devices where you can run tests.
 * Simulator - iOS Simulator
 * Emulator - Android Emulator (default Android emulators)
 * Android - Physical android device
 * iOS - Physical iOS device
 */
public enum DeviceType {
    Simulator, Emulator, Android, iOS
}
